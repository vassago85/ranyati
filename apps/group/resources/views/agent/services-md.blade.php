## Hunting motivations

- Page: {{ $base }}/firearm-licence-motivation-hunting

Hunting motivations support licence applications where the lawful purpose is
hunting. The motivation typically references the applicant's dedicated-hunter
status (where applicable), the species and terrain the firearm is intended
for, and the supporting documentation required by SAPS.

## Licence renewals

- Page: {{ $base }}/firearm-licence-renewal-south-africa

Renewal motivations keep existing firearm licences current under the
Firearms Control Act. We prepare renewal documentation well before licences
expire and address any changes in the applicant's circumstances since the
original application.

## Appeals and reviews

- Page: {{ $base }}/firearm-licence-appeal-south-africa

Appeals address refused applications; reviews address existing licence
conditions. Both require careful re-engagement with the original SAPS
reasoning and a properly structured legal-and-factual response.

