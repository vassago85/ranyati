## Firearm motivation letter (overview)

- Page: {{ $base }}/firearm-motivation-letter

A firearm motivation letter is the written document that accompanies a SAPS
firearm licence application or renewal. It explains the applicant's lawful
purpose for the firearm, the personal circumstances supporting that purpose,
and how the application meets the Firearms Control Act. SAPS weighs the
motivation alongside competency certificates, safe-storage verification,
background checks, and other requirements.

## Self-defence motivations

- Page: {{ $base }}/firearm-licence-motivation-self-defence

Self-defence motivations must reflect the applicant's actual circumstances
and lawful purpose under the Firearms Control Act (Act 60 of 2000). We help
applicants articulate a coherent, specific narrative supported by the facts
they supply. Generic or copied text weakens an application; the motivation
should read as authentic to the individual.

## Sport-shooting motivations

- Page: {{ $base }}/firearm-licence-motivation-sport-shooting

Sport-shooting motivations cover competitive disciplines such as PRS, IPSC,
action rifle, and target shooting. The motivation is typically aligned with
the applicant's dedicated-sport-shooter status (allocated through an
accredited association such as NRAPA) and their actual participation in
qualifying activities.

