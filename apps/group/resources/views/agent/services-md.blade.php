## Dedicated status (handled via NRAPA)

For dedicated sport shooter or dedicated hunter status, applicants engage
NRAPA — the SAPS-accredited association in the Ranyati Group:

- https://nrapa.ranyati.co.za

NRAPA handles association membership, activity records, and dedicated-status
allocation. Ranyati Motivations handles the written motivation that
accompanies the SAPS licence application itself.

## Estate firearm administration

Where firearms form part of a deceased estate, we assist with the
administrative motivation paperwork required for transfer, retention by an
heir, or surrender. The legal estate process itself remains with the
executor / attorney.

## What we do not do

- We do not issue firearm licences. Only SAPS issues firearm licences.
- We do not guarantee SAPS outcomes, turnaround times, or success rates.
- We do not provide legal advice or act as an attorney.
- We do not perform NRAPA association duties, secure firearm storage, or
  firearm sales — those sit with our sister brands.

