## Can you submit my application to SAPS for me?

Procedures vary by office and application type. We focus on preparing
high-quality documentation and guiding the applicant on submission steps;
the applicant remains the applicant of record with SAPS. Many clients still
attend the DFO in person with their pack.

## Will the motivation guarantee that SAPS approves my licence?

No. SAPS decides every firearm licence application. A well-researched and
properly structured motivation improves the quality of the application, but
no motivation provider — including Ranyati Motivations — can guarantee a
SAPS outcome. Anyone who guarantees an outcome should be treated with
caution.

