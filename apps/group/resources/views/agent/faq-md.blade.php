## What is a firearm licence motivation?

A motivation is the written motivation document that explains the
applicant's lawful purpose and personal circumstances for a firearm licence
application or renewal under the Firearms Control Act. SAPS uses it together
with competency, background checks, and other requirements.

## How long does motivation preparation take?

Timelines depend on how quickly the applicant provides accurate information
and supporting documents. Complex cases or renewals with changed
circumstances may take longer. We give a realistic estimate after an
initial review and do not promise dates we cannot meet.

