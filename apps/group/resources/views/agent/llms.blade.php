## What Ranyati Motivations is

Ranyati Motivations prepares the researched, structured, and Firearms Control
Act–aligned written motivation documents that accompany South African firearm
licence applications, renewals, and appeals. We do not issue licences — the
South African Police Service (SAPS) does. Our role is to help applicants
articulate a clear, lawful, factually accurate case for the licence category
they are applying for.

