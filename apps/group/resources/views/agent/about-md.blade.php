## What we do

Ranyati Motivations handles the written-motivation portion of the South
African firearm licensing process. SAPS makes every licence decision; our
role is to help applicants articulate a clear, lawful, factually accurate
case for the licence category they are applying for.

Our public service areas are:

- **New licence motivations** — self-defence, hunting, and sport-shooting
  disciplines (including PRS, IPSC, action rifle, and target shooting).
- **Licence renewals** — timely renewal motivations to keep existing firearm
  licences current.
- **Compliance support** — guidance on Firearms Control Act documentation
  and regulatory expectations.
- **Dedicated status applications** — motivations for dedicated sport
  shooter or dedicated hunter status through NRAPA (our sister association).
- **Appeals and reviews** — assistance with appeals against refused
  applications and reviews of existing licence conditions.
- **Estate administration** — assistance with the administration of
  firearms in deceased estates, including transfers and surrender processes.

## How we work

1. **Consultation.** We assess the applicant's requirements and advise on
   the appropriate licence category, supporting documents, and process.
2. **Documentation.** We prepare a comprehensive motivation aligned with the
   Firearms Control Act and the criteria the registrar typically considers.
3. **Submission & follow-up.** We guide the applicant through submission and
   provide ongoing support until the application is finalised. The applicant
   remains the applicant of record with SAPS.

## What we do not do

- We do **not** issue firearm licences. Only SAPS issues firearm licences.
- We do **not** guarantee SAPS outcomes, turnaround times, or success rates.
- We do **not** provide legal advice. For legal or regulatory questions,
  consult a qualified attorney or contact SAPS directly.
- We do **not** offer storage, association membership, or firearm sales.
  Those are handled by our sister brands listed below.

